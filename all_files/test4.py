# This is a sample Python file
# It contains a few methods and comments

def add_numbers(a, b):
    # This method adds two numbers and returns the result
    return a + b

def subtract_numbers(a, b):
    # This method subtracts two numbers and returns the result
    return a - b

def multiply_numbers(a, b):
    # This method multiplies two numbers and returns the result
    return a * b

# This is the main method
if __name__ == '__main__':
    # Call the methods and print the results
    print(add_numbers(1, 2))
    print(subtract_numbers(3, 4))
    print(multiply_numbers(5, 6))